/*
    Q.
    문제 설명
    로봇 청소기가 주어졌을 때, 청소하는 영역의 개수를 구하는 프로그램을 작성하시오.

    로봇 청소기가 있는 장소는 N×M 크기의 직사각형으로 나타낼 수 있으며, 1×1크기의 정사각형 칸으로 나누어져 있다. 각각의 칸은 벽 또는 빈 칸이다.
    청소기는 바라보는 방향이 있으며, 이 방향은 동, 서, 남, 북 중 하나이다. 
    지도의 각 칸은 (r, c)로 나타낼 수 있고, r은 북쪽으로부터 떨어진 칸의 개수, c는 서쪽으로 부터 떨어진 칸의 개수이다.

    로봇 청소기는 다음과 같이 작동한다.

    1. 현재 위치를 청소한다.
    2. 현재 위치에서 현재 방향을 기준으로 왼쪽방향부터 차례대로 탐색을 진행한다.
        a. 왼쪽 방향에 아직 청소하지 않은 공간이 존재한다면, 그 방향으로 회전한 다음 한 칸을 전진하고 1번부터 진행한다.
        b. 왼쪽 방향에 청소할 공간이 없다면, 그 방향으로 회전하고 2번으로 돌아간다.
        c. 네 방향 모두 청소가 이미 되어있거나 벽인 경우에는, 바라보는 방향을 유지한 채로 한 칸 후진을 하고 2번으로 돌아간다.
        d. 네 방향 모두 청소가 이미 되어있거나 벽이면서, 뒤쪽 방향이 벽이라 후진도 할 수 없는 경우에는 작동을 멈춘다.
    로봇 청소기는 이미 청소되어있는 칸을 또 청소하지 않으며, 벽을 통과할 수 없다.

    입력 조건
    로봇 청소기가 있는 칸의 좌표 (r, c)와 바라보는 방향 d가 주어진다. 
    이 때 d가 0인 경우에는 북쪽을, 1인 경우에는 동쪽을, 2인 경우에는 남쪽을, 3인 경우에는 서쪽을 바라보고 있는 것이다.

    또한 청소하고자 하는 방의 지도를 2차원 배열로 주어진다.
    빈 칸은 0, 벽은 1로 주어진다. 지도의 첫 행, 마지막 행, 첫 열, 마지막 열에 있는 모든 칸은 벽이다.

    로봇 청소기가 있는 칸의 상태는 항상 빈 칸이라고 했을 때,
    로봇 청소기가 청소하는 칸의 개수를 반환하시오.

*/
function turnLeft(d) {
  return (d + 3) % 4; // 왼쪽으로 회전 (북 → 서 → 남 → 동)
}

function getDirectionWhenGoBack(d) {
  return (d + 2) % 4; // 후진 방향 (반대 방향)
}

function solution(r, c, d, roomMap) {
  const dr = [-1, 0, 1, 0]; // 북, 동, 남, 서 방향 (행)
  const dc = [0, 1, 0, -1]; // 북, 동, 남, 서 방향 (열)

  const n = roomMap.length;
  const m = roomMap[0].length;

  let cleanedCount = 1; // 초기 위치 청소
  roomMap[r][c] = 2;

  const queue = [[r, c, d]];

  while (queue.length > 0) {
    const [currR, currC, currD] = queue.shift();
    let tempD = currD;

    for (let i = 0; i < 4; i++) {
      tempD = turnLeft(tempD);
      const newR = currR + dr[tempD];
      const newC = currC + dc[tempD];

      if (
        newR >= 0 &&
        newR < n &&
        newC >= 0 &&
        newC < m &&
        roomMap[newR][newC] === 0
      ) {
        cleanedCount++;
        roomMap[newR][newC] = 2;
        queue.push([newR, newC, tempD]);
        break;
      }

      // 네 방향 모두 청소가 되어있거나 벽인 경우
      if (i === 3) {
        const backD = getDirectionWhenGoBack(currD);
        const backR = currR + dr[backD];
        const backC = currC + dc[backD];

        if (roomMap[backR][backC] !== 1) {
          queue.push([backR, backC, currD]);
        } else {
          return cleanedCount; // 뒤가 벽이면 종료
        }
      }
    }
  }

  return cleanedCount;
}

